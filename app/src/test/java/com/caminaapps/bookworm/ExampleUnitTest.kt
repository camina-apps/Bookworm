 *
 * See [testing documentation](http://d.android.com/tools/testing).
 */
class ExampleUnitTest {
    @Test
    fun something_isCorrect() {
        assertThat("hello").matches("hello")
        assertThat(false).isFalse()
    }

    @Test
    fun on_isCorrect() {
        assertThat("hello").matches("hello")
        assertThat(false).isFalse()
    }

    @Test
    fun of_isCorrect() {
        assertThat("hello").matches("hello")
        assertThat(false).isFalse()
    }
}
